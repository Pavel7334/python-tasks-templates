# Дано множество A = {0, 1, 2, 6, 7, 8, 9} и B = {1, 3, 6, 10, 9, 21, 5}. 
# Используя операции над множествами, получите их объединение и 
# выведите результат в терминал.
def app(A: set[int], B: set[int]) -> None:
    print(A.union(B))


if __name__ == "__main__":
    A = {0, 1, 2, 6, 7, 8, 9}
    B = {1, 3, 6, 10, 9, 21, 5}
    app(A, B)
