# Дан кортеж my_tuple = (0, -2, 81, 42, 6, -6, 23). 
# Проверьте входит ли в него элемент со значением 6 и 
# выведите в терминал полученный результат. 
# Для решения задачи запрещено использовать циклы.
def app(my_tuple: tuple) -> None:
    print(6 in my_tuple)


if __name__ == "__main__":
    my_tuple = (0, -2, 81, 42, 6, 6, 23)
    app(my_tuple)
